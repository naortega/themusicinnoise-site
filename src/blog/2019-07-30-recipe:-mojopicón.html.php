<p>Mojopicón is a Spanish spicy sauce recipe. It goes extremely well with meats, fish, on sandwiches (including burgers and hot dogs), but is mostly used as a dip (really good with french fries). It's extremely simple to make, and quite cheap as well, although it has quite the variety of spices.</p>

<p>To make mojopicón you will require a blender, preferably a hand-held blender.</p>

<p>Ingredients:</p>
<ul>
	<li>1 cup of olive oil (aceite de oliva)</li>
	<li>1/8 cups of vinegar (vinagre)</li>
	<li>1/2 cups of water (agua)</li>
	<li>A pinch of cilantro</li>
	<li>2 small spoons of cumin (comino)</li>
	<li>1/2 small spoons of black pepper (pimienta)</li>
	<li>1 small spoon of paprika (pimentón)</li>
	<li>1 small spoon of shredded bread (pan rallado)</li>
	<li>5 teeth of garlic (ajo)</li>
	<li>3 spicy peppers (guindillas)</li>
	<li>1/2 small spoons of salt (sal)</li>
	<li>2 small spoons of oregano</li>
</ul>

<p>Once the ingredients are ready, all you have to do is blend them all together.</p>

<p>¡Que aproveche!</p>


